def add_2_numbers(a, b):
    """Add two numbers

    This function only returns the addition of two input numbers, which can
    be either `int` or `float` types.

    Parameters
    ----------
    a : int or float
        First number
    b : int or float
        Second number

    Returns
    -------
    int or float
        `a` plus `b`
    """
    return a + b
